$(document).on('click', '[data-set-session]', function (event) {
    event.preventDefault();
    const element = $(this);
    const params = element.data('params');
    const tipo = element.data('tipo');
    if (tipo == 'afegir') {
        $('.extra-info').hide('fast')
    } else {
        $('.extra-info').show('fast')
    }
    $.ajax({
        url: '/site/set-session?tipo=' + tipo + params,
        type: 'POST',
    }).done(() => {
    })
})

function insertParam(key, value) {
    key = encodeURI(key);
    value = encodeURI(value);

    var kvp = document.location.search.substr(1).split('&');

    var i = kvp.length;
    var x;
    while (i--) {
        x = kvp[i].split('=');

        if (x[0] == key) {
            x[1] = value;
            kvp[i] = x.join('=');
            break;
        }
    }

    if (i < 0) {
        kvp[kvp.length] = [key, value].join('=');
    }

    //this will reload the page, it's likely better to store this until finished
    document.location.search = kvp.join('&');
}

